public class Bytes_short_int_long {
    public static void main(String[] args) {
        byte myNum = 100;
        short myNum2 = 5000;
        int myNum3 = 100000;
        long myNum4 = 15000000000L;
        System.out.println(myNum);
        System.out.println(myNum2);
        System.out.println(myNum3);
        System.out.println(myNum4);

    }
}
